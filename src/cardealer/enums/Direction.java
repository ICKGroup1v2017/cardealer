package cardealer.enums;

/**
 * This enum dictates the movement type
 * @author ahmet
 */
public enum Direction {
 
   /**
    * No movements, parked
    */
    PARKING,
    /**
     * Movement FORWARD
     */
    FORWARD, 
     /**
     * Movement to LEFT
     */
    LEFT , 
     /**
     * Movement to RIGHT
     */
    RIGHT, 
    /**
     * Movement BACKWARD
     */
     BACKWARD, 
     /**
     * Movement UP
     */
    UP, 

    /**
     *  Movement DOWN
     */
    DOWN 
          
}
